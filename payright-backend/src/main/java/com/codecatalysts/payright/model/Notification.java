package com.codecatalysts.payright.model;

import lombok.Data;
import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.mapping.Document;
import java.time.LocalDateTime;

@Data
@Document(collection = "notifications")
public class Notification {
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public LocalDateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(LocalDateTime createdAt) {
        this.createdAt = createdAt;
    }

    public boolean isRead() {
        return read;
    }

    public void setRead(boolean read) {
        this.read = read;
    }

    public String getRelatedSubscriptionId() {
        return relatedSubscriptionId;
    }

    public void setRelatedSubscriptionId(String relatedSubscriptionId) {
        this.relatedSubscriptionId = relatedSubscriptionId;
    }

    @Id
    private String id;
    private String userId;
    private String message;
    private String type;
    private LocalDateTime createdAt;
    private boolean read = false;
    private String relatedSubscriptionId;
}
